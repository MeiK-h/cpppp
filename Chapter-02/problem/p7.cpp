#include <iostream>

using namespace std;

void show(int hours, int minutes) {
    cout << "Time: " << hours << ":" << minutes << endl;
}

int main(void) {
    int hours, minutes;
    cout << "Enter the number of hours: " << ends;
    cin >> hours;
    cout << "Enter the number of minutes: " << ends;
    cin >> minutes;
    show(hours, minutes);
    return 0;
}